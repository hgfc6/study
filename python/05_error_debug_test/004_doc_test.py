# 文档测试
# 文档测试是一种测试技术，它可以让你在编写代码时就测试代码的文档注释。
# 文档测试的目的是确保你的代码文档与代码保持一致，并能够提供清晰的、易于理解的说明。
# 文档测试的基本思路是：
# 1. 编写代码的文档注释，描述代码的功能、输入、输出、异常等。
# 2. 在代码的前面或后面，使用特殊的注释符号，比如“# doctest: +SKIP”，来标记文档测试的开始。
# 3. 在文档注释中，编写测试用例，描述代码的输入、输出、期望输出等。
# 4. 运行文档测试，如果测试用例通过，说明文档注释与代码实现是一致的，否则说明文档注释与代码实现存在差异。
# 文档测试的好处：
# 1. 文档测试可以帮助你编写清晰、易于理解的代码。
# 2. 文档测试可以帮助你发现代码中的错误。
# 3. 文档测试可以帮助你及早发现代码中的不一致性，并及时纠正。
# 4. 文档测试可以提高代码的可读性和可维护性。